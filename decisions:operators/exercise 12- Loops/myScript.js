
var x = 1;

//WHILE LOOP

document.write ("WHILE Loop <br>");

while ( x <= 5 ) {
  document.write(x + "<br>");

  x++;
}

document.write(" DO WHILE Loop <br>");

var y = 6;

do {

  document.write(y + "<br>");
  y++;

} while ( y <= 5);

document.write(" FOR Loop <br>");

//FOR LOOP

for ( z = 1; z <= 5; z++) {

  document.write(z + "<br>");  
}
